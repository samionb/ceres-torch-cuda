def build_problem(use_robust_loss: bool) -> tuple[tc.Problem, torch.Tensor, torch.Tensor]:
    m = torch.tensor([0.0], dtype=torch.float64)
    c = torch.tensor([0.0], dtype=torch.float64)
    problem = tc.Problem()
    for x, y in DATA:
        loss = tc.CauchyLoss(0.5) if use_robust_loss else None
        problem.add_residual_block(
            tc.AutoDiffCostFunction(lambda m, c, x=x, y=y: y - torch.exp(m[0] * x + c[0]), [1, 1]),
            loss,
            [m, c],
        )
    return problem, m, c


def main() -> None:
    problem, m, c = build_problem(use_robust_loss=True)
    summary = tc.solve(tc.SolverOptions(max_num_iterations=25), problem)
    print(summary.BriefReport())
    print(f"m={m.item():.8f} c={c.item():.8f}")


if __name__ == "__main__":
    main()

